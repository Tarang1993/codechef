import java.util.Scanner;
class Solution {
    public static void main(String[] args)
 
    {
 
        int i,j,n;
 
        Scanner s=new Scanner(System.in);
 
        n=s.nextInt();
 
        long[][] a=new long[n][2];
 
        for(i=0;i<n;i++)
 
        {
 
          for(j=0;j<2;j++)
 
          {
 
             a[i][j]=s.nextLong(); 
 
	  }
            if(a[i][1]!=0)
            {
                System.out.print((a[i][0]/a[i][1]));
                System.out.print(" ");
                System.out.print((a[i][0]%a[i][1]));
                System.out.println();
            }
            else
            {
                System.out.print("0");
                System.out.print(" ");
                System.out.print(a[i][0]);
                System.out.println();
            }
 
            }
        }
 
    } 
